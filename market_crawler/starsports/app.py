@cache
def page_url(*, current_url: str, next_page_no: int) -> str:
    if "?page" in current_url:
        return current_url.replace(f"?page={next_page_no-1}", f"?page={next_page_no}")
    if "&page" in current_url:
        return current_url.replace(f"&page={next_page_no-1}", f"&page={next_page_no}")

    return f"{current_url}&page={next_page_no}"


async def find_categories(browser: PlaywrightBrowser):
    full_categories: list[Category] = []

    page = await browser.new_page()
    await visit_link(page, "https://starsportsmall.co.kr/main/#")

    categories = await page.query_selector_all(
        "#gnb > ul:nth-child(1) > li:nth-child(1) > div > ul > li"
    )

    for category in categories:
        if not (el := await category.query_selector("a")):
            continue

        if not (category_text := await el.text_content()):
            continue

        if not (category_page_url := await el.get_attribute("href")):
            continue

        category_page_url = urljoin(page.url, category_page_url)

        full_categories.append(Category(category_text, category_page_url))

    await page.close()

    async with AIOFile("categories.txt", "w", encoding="utf-8-sig") as afp:
        await afp.write(
            "{}".format("\n".join(f"{cat.name}, {cat.url}" for cat in full_categories))
        )

    return full_categories


